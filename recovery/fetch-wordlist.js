/**
 * fetch-wordlist.js
 * Run once in Termux to generate wordlist.js from the official BIP39 source.
 *
 * Usage:
 *   node recovery/fetch-wordlist.js
 *
 * Verifies the SHA256 of the fetched list against the known-good hash
 * before writing the file — so you're not trusting a MITM'd wordlist.
 */

const https = require("https");
const crypto = require("crypto");
const fs = require("fs");
const path = require("path");

const SOURCE = "https://raw.githubusercontent.com/trezor/python-mnemonic/master/src/mnemonic/wordlist/english.txt";

// SHA256 of the canonical BIP39 English wordlist (verify independently if paranoid)
const KNOWN_HASH = "2f5eed53a4727b4bf8880d8f3f199efc90e58503646d9ff8eff3a2ed3b24dbda";

const OUT = path.join(__dirname, "wordlist.js");

https.get(SOURCE, (res) => {
  let raw = "";
  res.on("data", chunk => raw += chunk);
  res.on("end", () => {
    const words = raw.trim().split("\n").map(w => w.trim()).filter(Boolean);

    if (words.length !== 2048) {
      console.error(`ERROR: expected 2048 words, got ${words.length}`);
      process.exit(1);
    }

    // Verify hash
    const hash = crypto.createHash("sha256").update(raw.trim()).digest("hex");
    if (hash !== KNOWN_HASH) {
      console.error(`SECURITY: hash mismatch!\n  got:      ${hash}\n  expected: ${KNOWN_HASH}`);
      console.error("Do NOT use this wordlist. Fetch may have been tampered with.");
      process.exit(1);
    }

    const content = `// Auto-generated by fetch-wordlist.js — DO NOT EDIT\n// Source: BIP39 English wordlist (2048 words)\n// SHA256: ${hash}\nconst WORDLIST = ${JSON.stringify(words, null, 2)};\nexport default WORDLIST;\n`;

    fs.writeFileSync(OUT, content, "utf8");
    console.log(`✓ wordlist.js written (${words.length} words, hash verified)`);
  });
}).on("error", err => {
  console.error("Fetch failed:", err.message);
  process.exit(1);
});
